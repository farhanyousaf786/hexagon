  options(String option, String url) {
    return GestureDetector(
      onTap: () => {
        _launchURL(url),
      },
      child: Padding(
        padding: const EdgeInsets.all(8),
        child: SwitchText(
          colors: Colors.blue,
          defualtfSize: 14,
          dFontSize: 16,
          tFontSize: 14,
          mFontSize: 13,
          text: "               > $option",
          textAlign: TextAlign.center,
          fontWeight: FontWeight.bold,
        ),
      ),
    );
  }

  _launchURL(String url) async {
    if (await canLaunch(url)) {
      await launch(url);
    } else {
      throw 'Could not launch $url}';
    }
  }
